export const PI_MAX_POSITION = PI_DIGITS.length

export function validatePiAnswer(position: number, answer: string): boolean {
  if (position < 1 || position > PI_MAX_POSITION) return false
  return PI_DIGITS[position - 1] === answer.trim()
}

export function getPiDigit(position: number): string {
  if (position < 1 || position > PI_MAX_POSITION) throw new RangeError(`Pi position ${position} out of range`)
  return PI_DIGITS[position - 1]
}
